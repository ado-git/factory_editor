    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        // TODO add your handling code here:
        try {
            String var_name = InputOutput.getString(jTextVarName, "valor de var_name invalido");
            String comment = jTextComment.getText();
            String d_value = jTextPaneD_value.getText();
            
            ComboBoxBasicManager combo_mgr = new ComboBoxBasicManager(jComboBoxTypes);
            
            String type = combo_mgr.getValue("valor de type invalido");
            
            Variable v;
            
            if(type.compareTo(EnumBasico.ARRAY.toString()) == 0)
            {
                String[] fields = InputOutput.getSplit(jTextFields, null);
                
                v = new Variable(var_name, comment, type, d_value, fields);
            }
            else
            {
                v = new Variable(var_name, comment, type, d_value, null);
            }
            
            VariableIO io = Config.getInstance().getVariableIO_newInstance();
            
            if(final_input_variable == null)
            {   
                io.addVariable(v);
            }
            else
            {
                io.saveVariable(v);
            }
            
            io.saveFile();
            
            close();
            
        } catch (Exception e) {
            Dialogos.showMessageDialog(e.getMessage());
        }
    }//GEN-LAST:event_jButtonGuardarActionPerformed

