    public PanelVariableEditor(String var_name) throws Exception {
        super("Editor de variable");
        initComponents();
        
        this.final_input_variable = var_name;
        
        ComboBoxBasicManager combo_mgr = new ComboBoxBasicManager(jComboBoxTypes);
        
        combo_mgr.init();
        
        for(EnumBasico e : EnumBasico.values())
        {
            combo_mgr.addItem(e.toString());
        }
        
        if(final_input_variable!= null)
        {
            jTextVarName.setText(final_input_variable);
            jTextVarName.setEnabled(false);
            
            loadVariable();//este asigna valores a los controles, el combobox tiene que estar configurado antes!!!
        }
        else
        {
            jButtonEliminar.setEnabled(false);
        }
    }
    
    private void loadVariable() throws Exception
    {
        Variable v = Config.getInstance().getVariableIO_newInstance().getVariable(final_input_variable);
        
        jTextVarName.setText(v.getName());
        jTextComment.setText(v.getDescripcion());
        jTextPaneD_value.setText(v.getD_value());
        
        if(v.getTipo().getFields() != null && v.getTipo().getFields().length > 0)
            jTextFields.setText(String.join(",", v.getTipo().getFields()));
        
        jComboBoxTypes.setSelectedItem(v.getTipo().getTipoBasico().toString());
    }
    
    /**
     * Consulta el combobox para saber si la variable es de tipo array
     */
    private boolean isType_Array()
    {
        return jComboBoxTypes.getSelectedItem().toString().compareTo(EnumBasico.ARRAY.toString()) == 0;
    }

