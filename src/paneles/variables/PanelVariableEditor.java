    private void jMenuItemConvertComaSeparatedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemConvertComaSeparatedActionPerformed
        // TODO add your handling code here:
        String text = jTextPaneD_value.getText();
        String[] fields = null;
        
        try {
            ArrayList<HashMap<String, String>> alist_fields = new CommaSeparatedExtractor(jTextFields.getText(), null).parse();
            
            fields = new String[alist_fields.size()];
            
            for (int i = 0; i < fields.length; i++) {
                fields[i] = alist_fields.get(i).get(CommaSeparatedExtractor.SINGLEITEMINDICATOR);
            }
            
        } catch (Exception e) {
            Dialogos.showMessageDialog("Problema con los fields: " + e.getMessage());
            return;
        }
        
        try {
            
            CommaSeparatedExtractor conv = new CommaSeparatedExtractor(text, fields);
            
            ArrayCodeCreator c = new ArrayCodeCreator(conv.parse(), CommaSeparatedExtractor.SINGLEITEMINDICATOR);
            
            jTextPaneD_value.setText(c.create());
            
        } catch (Exception e) {
            Dialogos.showMessageDialog(e.getMessage());
        }
    }//GEN-LAST:event_jMenuItemConvertComaSeparatedActionPerformed


